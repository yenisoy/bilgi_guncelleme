/**
 * Fetch place details from Google Places API
 */
async function fetchFromGoogle(query) {
    if (!GOOGLE_MAPS_API_KEY) {
        console.log('Google Maps API key not set');
        return null;
    }

    const url = `https://maps.googleapis.com/maps/api/place/textsearch/json?query=${encodeURIComponent(query)}&language=tr&key=${GOOGLE_MAPS_API_KEY}`;

    try {
        const response = await fetch(url);
        const data = await response.json();

        if (data.status === 'OK' && data.results.length > 0) {
            return data.results[0];
        }
        return null;
    } catch (error) {
        console.error('Google API error:', error);
        return null;
    }
}

/**
 * Fetch autocomplete suggestions
 */
async function fetchAutocomplete(input, types = '(regions)') {
    if (!GOOGLE_MAPS_API_KEY) return [];

    const url = `https://maps.googleapis.com/maps/api/place/autocomplete/json?input=${encodeURIComponent(input)}&types=${types}&components=country:tr&language=tr&key=${GOOGLE_MAPS_API_KEY}`;

    try {
        const response = await fetch(url);
        const data = await response.json();
        return data.status === 'OK' ? data.predictions : [];
    } catch (error) {
        console.error('Google Autocomplete error:', error);
        return [];
    }
}

/**
 * Get all provinces
 */
async function getProvinces() {
    // Check cache
    const cached = await AddressCache.find({ type: 'province' }).sort({ name: 1 });

    if (cached.length >= 81) {
        return cached.map(p => ({ id: p.placeId, name: p.name }));
    }

    // Use static data and cache
    console.log('Caching provinces...');
    const provinces = [];

    for (const provinceName of Object.keys(TURKEY_DATA)) {
        const placeId = `province_${provinceName.toLowerCase().replace(/\s/g, '_')}`;

        await AddressCache.findOneAndUpdate(
            { placeId },
            { type: 'province', placeId, name: provinceName },
            { upsert: true, new: true }
        );

        provinces.push({ id: placeId, name: provinceName });
    }

    return provinces.sort((a, b) => a.name.localeCompare(b.name, 'tr'));
}

/**
 * Get districts for a province
 */
async function getDistricts(provinceId) {
    // Get province
    const province = await AddressCache.findOne({ placeId: provinceId, type: 'province' });
    if (!province) return [];

    // Check cache
    const cached = await AddressCache.find({ type: 'district', parentId: provinceId }).sort({ name: 1 });

    if (cached.length > 0) {
        return cached.map(d => ({ id: d.placeId, name: d.name }));
    }

    // Get from static data
    const districtNames = TURKEY_DATA[province.name] || [];
    console.log(`Caching districts for ${province.name}...`);

    const districts = [];
    for (const districtName of districtNames) {
        const placeId = `district_${provinceId}_${districtName.toLowerCase().replace(/\s/g, '_')}`;

        await AddressCache.findOneAndUpdate(
            { placeId },
            { type: 'district', parentId: provinceId, placeId, name: districtName },
            { upsert: true, new: true }
        );

        districts.push({ id: placeId, name: districtName });
    }

    return districts.sort((a, b) => a.name.localeCompare(b.name, 'tr'));
}

/**
 * Get neighborhoods for a district - uses Google API
 */
async function getNeighborhoods(provinceId, districtId) {
    const district = await AddressCache.findOne({ placeId: districtId, type: 'district' });
    const province = await AddressCache.findOne({ placeId: provinceId, type: 'province' });

    if (!district || !province) return [];

    // Check cache
    const cached = await AddressCache.find({ type: 'neighborhood', parentId: districtId }).sort({ name: 1 });

    if (cached.length > 0) {
        return cached.map(n => ({ id: n.placeId, name: n.name }));
    }

    // Fetch from Google
    console.log(`Fetching neighborhoods for ${district.name}, ${province.name}...`);
    const query = `${district.name} ${province.name} mahallesi`;
    const predictions = await fetchAutocomplete(query, '(regions)');

    const neighborhoods = [];
    const seen = new Set();

    for (const prediction of predictions.slice(0, 30)) {
        let name = prediction.structured_formatting?.main_text || prediction.description.split(',')[0];

        // Clean up name
        name = name.replace(/\s*Mahallesi?$/i, '').trim();
        if (!name || seen.has(name.toLowerCase())) continue;
        seen.add(name.toLowerCase());

        const placeId = prediction.place_id || `neighborhood_${districtId}_${name.toLowerCase().replace(/\s/g, '_')}`;

        await AddressCache.findOneAndUpdate(
            { placeId },
            { type: 'neighborhood', parentId: districtId, placeId, name: name + ' Mahallesi' },
            { upsert: true, new: true }
        );

        neighborhoods.push({ id: placeId, name: name + ' Mahallesi' });
    }

    // If Google didn't return results, add a placeholder
    if (neighborhoods.length === 0) {
        const placeId = `neighborhood_${districtId}_merkez`;
        await AddressCache.findOneAndUpdate(
            { placeId },
            { type: 'neighborhood', parentId: districtId, placeId, name: 'Merkez Mahallesi' },
            { upsert: true, new: true }
        );
        neighborhoods.push({ id: placeId, name: 'Merkez Mahallesi' });
    }

    return neighborhoods.sort((a, b) => a.name.localeCompare(b.name, 'tr'));
}

/**
 * Get streets for a neighborhood - uses Google API
 */
async function getStreets(provinceId, districtId, neighborhoodId) {
    const neighborhood = await AddressCache.findOne({ placeId: neighborhoodId, type: 'neighborhood' });

    if (!neighborhood) return [];

    // Check cache
    const cached = await AddressCache.find({ type: 'street', parentId: neighborhoodId }).sort({ name: 1 });

    if (cached.length > 0) {
        return cached.map(s => ({ id: s.placeId, name: s.name }));
    }

    // Fetch from Google
    console.log(`Fetching streets for ${neighborhood.name}...`);
    const predictions = await fetchAutocomplete(`${neighborhood.name} sokak cadde`, 'route');

    const streets = [];
    const seen = new Set();

    for (const prediction of predictions.slice(0, 20)) {
        const name = prediction.structured_formatting?.main_text || prediction.description.split(',')[0];

        if (!name || seen.has(name.toLowerCase())) continue;
        seen.add(name.toLowerCase());

        const placeId = prediction.place_id || `street_${neighborhoodId}_${name.toLowerCase().replace(/\s/g, '_')}`;

        await AddressCache.findOneAndUpdate(
            { placeId },
            { type: 'street', parentId: neighborhoodId, placeId, name },
            { upsert: true, new: true }
        );

        streets.push({ id: placeId, name });
    }

    return streets.sort((a, b) => a.name.localeCompare(b.name, 'tr'));
}

